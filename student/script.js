          borderAccent: "border-yellow-300",
          bg: "bg-gradient-to-b from-amber-950/40 via-[#0a0e1c]/95 to-amber-950/20",
          shadow: "shadow-[0_0_35px_rgba(245,158,11,0.3)]",
          pedestal: "from-yellow-400 via-amber-500 to-yellow-600",
